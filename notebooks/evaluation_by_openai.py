# 👇 Update these to match your actual column names
ID_COL          = df.columns[0]   # ID column
GENERATED_COL   = df.columns[3]   # Claude-generated paraphrase
GROUNDTRUTH_COL = df.columns[2]   # Ground truth paraphrase
PROFILE_COL     = df.columns[4]   # Full original unmasked profile

print(f"ID column                   : '{ID_COL}'")
print(f"Generated paraphrase column : '{GENERATED_COL}'")
print(f"Ground truth column         : '{GROUNDTRUTH_COL}'")
print(f"Profile column              : '{PROFILE_COL}'")

generated   = df[GENERATED_COL].fillna('').astype(str).tolist()
groundtruth = df[GROUNDTRUTH_COL].fillna('').astype(str).tolist()
profiles    = df[PROFILE_COL].fillna('').astype(str).tolist()

# ── Config ──────────────────────────────────────────────────────
MODEL         = "gpt-4.1"
PROGRESS_FILE = "evaluation_progress_openai.json"
OUTPUT_FILE   = "Evaluation_Output_OpenAI.xlsx"
DELAY         = 0.4
MAX_RETRIES   = 3

# ── Resume support ───────────────────────────────────────────────
if Path(PROGRESS_FILE).exists():
    with open(PROGRESS_FILE) as f:
        results = json.load(f)
    print(f"↩️  Resuming — {len(results)} rows already done.")
else:
    results = {}
# ── Parse selection from DeepSeek's response ───────────────────────────────
def parse_selection(text: str) -> str:
    """Extract Para→model or GT from response."""
    t = text.upper()
    if re.search(r'\bPARA\b', t):
        return 'model'
    if re.search(r'\bGT\b', t):
        return 'GT'
    if 'GENERATED' in t and 'MORE PERSONAL' in t:
        return 'model'
    if 'GROUND TRUTH' in t and 'MORE PERSONAL' in t:
        return 'GT'
    return 'UNCLEAR'

# ── Evaluation function ───────────────────────────────────────────
def evaluate(gen: str, gt: str, profile: str) -> str:
    prompt = (
        "1. [Generated paraphrase from model]\n"
        f"{gen}\n\n"
        "2. [Ground truth paraphrase]\n"
        f"{gt}\n\n"
        "Which one do you think is more personalised for the following "
        "profile? Give me your selection as (Para/GT) and justification in short in another column.\n\n"
        f"{profile}"
    )
    for attempt in range(1, MAX_RETRIES + 1):
        try:
            response = client.chat.completions.create(
                model=MODEL,
                messages=[{"role": "user", "content": prompt}]
            )
            return response.choices[0].message.content.strip()
        except Exception as e:
            if attempt < MAX_RETRIES:
                time.sleep(2 ** attempt)
            else:
                return f"ERROR: {e}"

# ── Main loop ───────────────────────────────────────────────────
total      = len(generated)
errors     = 0
skipped    = 0
start_time = time.time()

for i, (gen, gt, profile) in enumerate(zip(generated, groundtruth, profiles)):
    key = str(i)
    if key in results:
        continue

    # ── Skip if generated paraphrase is missing ───────────────────────
    if not str(gen).strip():
        results[key] = "SKIPPED: no generated paraphrase"
        skipped += 1
        continue

    out = evaluate(gen, gt, profile)
    results[key] = out

    if out.startswith("ERROR:"):
        errors += 1

    done    = len(results)
    elapsed = time.time() - start_time
    rate    = (done - skipped) / elapsed if elapsed > 0 else 0.001
    eta_min = (total - done) / rate / 60 if rate > 0 else 0
    pct     = done / total * 100
    bar     = "█" * int(pct / 2) + "░" * (50 - int(pct / 2))

    if done % 25 == 0:
        with open(PROGRESS_FILE, "w") as f:
            json.dump(results, f)

    if done % 5 == 0 or done == total:
        clear_output(wait=True)
        print("📊 Personalisation Evaluator (OpenAI) — Progress")
        print(f"[{bar}] {pct:.1f}%")
        print(f"  Rows done : {done} / {total}")
        print(f"  Evaluated : {done - skipped - errors}")
        print(f"  Skipped   : {skipped}")
        print(f"  Errors    : {errors}")
        print(f"  Speed     : {rate:.2f} rows/sec")
        print(f"  ETA       : {eta_min:.1f} minutes")
        print(f"  Last row  : #{i+1} — {'⏭ skipped' if not str(gen).strip() else ('✓' if not out.startswith('ERROR') else '✗')}")

    time.sleep(DELAY)

# ── Save progress file ──────────────────────────────────────────
with open(PROGRESS_FILE, "w") as f:
    json.dump(results, f)

# ── Build final output DataFrame with exact column order ───────
openai_evaluations = [results.get(str(i), "") for i in range(total)]
selections = [
    parse_selection(r) if not str(r).startswith(("SKIPPED", "ERROR")) else r
    for r in openai_evaluations
]

